#include<iostream>

using namespace std;

//relational operator;
int main(){
	int a = 10;
	int b = 5;

	cout << (a == b) << endl;
	cout << (a != b) << endl;
	cout << (a > b) << endl;
	cout << (a < b) << endl;
	cout << (a >= b) << endl;
	cout << (a <= b) << endl;

	cout << !(a <= b) << endl;

	return 0;
}


