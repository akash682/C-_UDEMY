#include<iostream>

using namespace std;

int main(){
	int a = 10;
	int b = 4;

	cout << a + b << endl;
	cout << a - b << endl;
	cout << a * b << endl;
	cout << a / b << endl;
	cout << a % b << endl;

	//incrementation, decrementation
	int c = 1;
	c++;
	cout << c++ << endl;
	cout << c << endl;

	int c1 = 1;

	c1++;
	cout << ++c1 << endl;
	cout << c1 << endl;

	// /= -= %= +=
}


